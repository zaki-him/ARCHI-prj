    CALL WAIT_6S
    CALL CLEAR_SCREEN


    ; FINAL: Display Reflected Matrix

    LEA DX, msg_final
    CALL DISPLAY_MESSAGE
    CALL display_matrix
    CALL PAUSE_SCREEN

    ; ????????? Display End Message ?????????
    LEA DX, msg_end
    CALL DISPLAY_MESSAGE

    ; Exit program
    MOV AH, 4Ch
    INT 21h


; UTILITY PROCEDURES

CLEAR_SCREEN PROC
    PUSH AX
    MOV AH, 00h
    MOV AL, 03h                    ; Set text mode and clear screen
    INT 10h
    POP AX
    RET
CLEAR_SCREEN ENDP

WAIT_6S PROC
    PUSH AX
    PUSH BX
    PUSH CX
    PUSH DX
    PUSH SI

    MOV AH, 00h                    ; Read BIOS timer tick count
    INT 1Ah
    MOV BX, DX                     ; Save target low word
    MOV SI, CX                     ; Save target high word
    ADD BX, 110                    ; Wait roughly 6 seconds at 18.2 Hz
    JC WAIT6S_INC_HIGH
    JMP WAIT6S_SET_TARGET
WAIT6S_INC_HIGH:
    INC SI
WAIT6S_SET_TARGET:

WAIT6S_LOOP:
    MOV AH, 00h
    INT 1Ah
    CMP CX, SI
    JA WAIT6S_DONE
    JB WAIT6S_LOOP
    CMP DX, BX
    JB WAIT6S_LOOP
WAIT6S_DONE:

    POP SI
    POP DX
    POP CX
    POP BX
    POP AX
    RET
WAIT_6S ENDP

DISPLAY_MESSAGE PROC
    PUSH AX
    MOV AH, 09h
    INT 21h
    POP AX
    RET
DISPLAY_MESSAGE ENDP

PAUSE_SCREEN PROC
    PUSH AX
    PUSH DX
    MOV AH, 08h                    ; Read character without echo
    INT 21h
    POP DX
    POP AX
    RET
PAUSE_SCREEN ENDP

CODE ENDS
END MAIN
