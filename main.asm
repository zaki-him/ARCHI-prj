MAIN:
    ; Initialize data segment
    MOV AX, DATA
    MOV DS, AX
    MOV ES, AX

    ; Clear screen
    MOV AH, 00h
    MOV AL, 03h
    INT 10h

    ; ????????? Display Title ?????????
    LEA DX, msg_title
    MOV AH, 09h
    INT 21h

    CALL CLEAR_SCREEN


    ; TASK 1: Display Original Matrix

    LEA DX, msg_task1
    CALL DISPLAY_MESSAGE
    CALL display_matrix
    CALL WAIT_6S
    CALL CLEAR_SCREEN


    ; TASK 2: Clean Matrix

    LEA DX, msg_task2
    CALL DISPLAY_MESSAGE
    CALL CleanMatrix
    CALL display_matrix
    CALL WAIT_6S
    CALL CLEAR_SCREEN


    ; TASK 3: Threshold Processing

    LEA DX, msg_task3
    CALL DISPLAY_MESSAGE
    CALL tache3
    CALL display_matrix
    CALL WAIT_6S
    CALL CLEAR_SCREEN


    ; TASK 4: Display Matrix with Row Sums

    LEA DX, msg_task4
    CALL DISPLAY_MESSAGE
    CALL TACHE4
    CALL WAIT_6S
    CALL CLEAR_SCREEN


    ; TASK 5: Column Reduction (Column Sums)

    LEA DX, msg_task5
    CALL DISPLAY_MESSAGE
    CALL TACHE4
    CALL COLUMN_REDUCTION
    CALL WAIT_6S
    CALL CLEAR_SCREEN


    ; TASK 6: Horizontal Reflection

    LEA DX, msg_task6
    CALL DISPLAY_MESSAGE
    CALL HORIZ_REFLECT
